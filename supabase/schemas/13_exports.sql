create policy export_job_read_own
  on public.export_job
  for select
  using (requested_by = auth.uid());

create policy export_job_read_authorized
  on public.export_job
  for select
  using (public.authorize('profiles.read'));

create policy export_job_insert_own
  on public.export_job
  for insert
  with check (
    requested_by = auth.uid()
    and public.authorize('profiles.read')
  );

create policy export_job_update_own
  on public.export_job
  for update
  using (requested_by = auth.uid() and public.authorize('profiles.read'))
  with check (requested_by = auth.uid() and public.authorize('profiles.read'));

